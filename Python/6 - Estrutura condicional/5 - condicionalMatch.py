#Entrada de dados: Recebe um número inteiro
dia = int(input("Informe o número do dia da semana: "))

match dia:
    case 1:
        print("Domingo")
    case 2:
        print("Segunda-feira")
    case 3:
        print("Terça-feira")
    case 4:
        print("Quarta-feira")
    case 5:
        print("Quita-feira")
    case 6:
        print("Sexta-feira")
    case 7:
        print("Sábado")
    case _: 
        print("O dia informado não é válido")
    