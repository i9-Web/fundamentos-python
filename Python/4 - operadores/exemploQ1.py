#Questão  1
#Resolva a expressão 2a + 4b + 5, para a = -6 e b = 3
#Resposta:
#Entrada de dados
a = int(input("Informe o valor de a (Ex: a = -6): "))
b = int(input("Informe o valor de b (Ex: b = 3): "))

#Processamento
resultado = 2 * a + 4 * b + 5

#Saída de dados
print(f"O resultado da expressão 2a + 4b + 5 é: 2 * {a} + 4 * {b} + 5 = {resultado}")
