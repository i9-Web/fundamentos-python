#Exemplos de variáveis com diferentes tipos de dados 
peso = 75.5                         #float
quantidade = 15                     #int
nome_produto = "Laptop"             #str
disponibilidade = True              #bool
precos = [10.0, 25.5, 40.0]         #list
cores = {"Primária": "azul", 
         "Secundária": "Verde"}     #dict

#Imprimindo múltiplas variáveis formatadas (f-string)
print(f"O peso informado foi: {peso}")
print(f"A quantidade informada foi: {quantidade}")
print(f"O nome do produto é: {nome_produto}")
print(f"A situação de disponibilidade do produto é: {disponibilidade}")
print(f"Os preços cadastrados são: {precos}")
print(f"As cores do projeto são: {cores}")