#Exemplos 1: Criação de um  set básico em python
# Criando sets de diferentes formas
numeros = {1, 2, 3, 4, 5}
print(f"Set de números: {numeros}")

# Duplicatas são removidas
cores = set(["azul", "verde", "vermelho", "azul"]) 
# Note que "azul" aparece apenas uma vez
print(f"Set de cores: {cores}") 
