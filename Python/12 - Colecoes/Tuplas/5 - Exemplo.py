#Exemplos 5: Tupla com um único elemento python
#Declarando um atupla de um único elemento 
tupla_unica = (42,) # Note a vírgula após o elemento
print(f"Tupla com um elemento: {tupla_unica}")
print(f"Tipo: {type(tupla_unica)}")

# Sem vírgula seria apenas um valor entre parênteses
nao_tupla = (42)
print(f"Sem a virgura no final não é uma Tupla: {nao_tupla}")
print(f"Não é tupla: {type(nao_tupla)}")