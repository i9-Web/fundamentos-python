#Exemplos 7: Tupla aninhada (tuplas dentro de tuplas) python
#Declarando um atupla de números 
estudantes = (
    ("Ana", 8.5),
    ("Bruno", 9.2),
    ("Carlos", 7.8)
)
print(f"Todos os estudantes: {estudantes}")
print(f"Nota da Ana: {estudantes[0][1]}")
print(f"Nota da Bruno: {estudantes[1][1]}")
print(f"Nota da Carlos: {estudantes[2][1]}")


